    private void jButtonAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAsignarActionPerformed
        String titulo = jTextTitulo.getText();
        String descripcion = jTextDescripcion.getText();
        String fecha = dateChooserEntrega.getText();
        
        if (titulo.isEmpty() || descripcion.isEmpty() || fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
            return;
        }
        
        try (PreparedStatement stmt = conexion.prepareStatement("INSERT INTO tareas (titulo, descripcion, fecha_entrega) VALUES (?, ?, ?)")) {
            stmt.setString(1, titulo);
            stmt.setString(2, descripcion);
            stmt.setString(3, fecha);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Tarea agregada correctamente");
            cargarTareas();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al agregar tarea: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonAsignarActionPerformed

